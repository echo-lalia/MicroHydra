# As a small optimization, we can use a special 'period' (and period repeat) for when the audio is real-time. 
PERIOD_C4 = const(b'\x01\x01\x01\x01\x01\x01\x01\x01')



def file_exists(path: str) -> bool:
    """Returns whether or not the path points to an existing file."""
    _FILE_FLAG = const(0x8000)
    try:
        return os.stat(path)[0] == _FILE_FLAG
    except OSError:
        return False



class Source:
    """Base Source class.
    
    Handles reading a specific type of data,
    and filling a buffer with converted samples on request.

    Properties:

    `file`:
        If in file mode, this is the loaded file.
    `file_mode`:
        Whether or not we are reading from a file.
    `file_start_position`:
        If in file mode, this is the start position we are reading from.
    `buf`:
        In file mode, the actual buffer we are loading file data into. (This is None if initialized with a MemoryView.)
    `mv`:
        The memoryview we are reading data from.
    `mv_end`:
        The real length of data stored in the buffer (might be shorter than `mv` length in file mode).
    `frame_idx`:
        The current index of the audio frame in `mv`.
    `period`:
        Controls the rate that frame_idx advances.
    `period_idx`:
        The current position we are reading from in our 'period'.
    `period_repeat`:
        The number of times to apply our period to our `frame_idx` (controls the octave of our note).
    `loop`:
        Whether or not the source is set to loop (or just end).
    `volume`:
        The volume of this Source; should be an integer from 0-10 (inclusive).
    `finished`:
        Whether or not the audio source has finished.
    """
    def __init__(
            self,
            data,  # A file, path to a file, or a memoryview
            frame_idx: int = 0,
            note: int = 0,
            octave: int = 4,
            period_idx: int = 1,
            loop: bool = False,
            volume: int = 10,
            buf_size: int = 512,
    ):
        """Initialize the audio source.
        `data`:
            a file, filepath, or memoryview to use as sample data.
        `frame_idx`:
            the index for the current audio frame.
        `note`:
            the index of the current note's period in `PERIODS`.
        `octave`:
            the octave of our note (along with `note`, this controls the `period_repeat`).
        `period_idx`:
            the current index inside the period.
        `loop`:
            whether or not to loop the sample.
        `volume`:
            the volume of the sample.
        `buf_size`:
            in file mode, the size of sample buffer to read into.
        """
        # Use data from a memoryview
        if isinstance(data, memoryview):
            self.file = None
            self.file_mode = False
            self.file_start_position = 0
            self.buf = None
            self.mv = data
            self.mv_end = len(self.mv)
        # Use data as a file path
        elif isinstance(data, str) and file_exists(data):
            self.file = open(data, 'rb')
            self.file_mode = True
            self.file_start_position = 0
            self.buf = bytearray(buf_size)
            self.mv = memoryview(self.buf)
            self.mv_end = 0
        # Use data as a file object
        elif hasattr(data, 'tell') and hasattr(data, 'seek') and hasattr(data, 'readinto'):
            self.file = data
            self.file_mode = True
            self.file_start_position = data.tell()
            self.buf = bytearray(buf_size)
            self.mv = memoryview(self.buf)
            self.mv_end = 0
        else:
            raise AttributeError(f"Source data {data} is not a recognized source.")
        
        self.frame_idx = frame_idx
        self.period_idx = period_idx
        self.loop = loop
        self.volume = volume
        self.finished = False
        self.set_note(note, octave)

        if self.file_mode:
            self.load_from_file()


    def set_note(self, note: int = 0, octave: int = 4):
        """Set the note and octave for this source.
        
        - note:
            Numerical 0-12 mapping from C-0 to B-0.
            Numbers outside that range will affect octave as well.
        - octave:
            Octave to play the sample at.
            By default C-4 which corresponds to unalterated sample.
        """
        # Special case for C-4 (no time stretching)
        if note + octave*12 == 48:
            self.period = PERIOD_C4
            self.period_repeat = 1

        else:
            self.period = PERIODS[note % 12]
            self.period_repeat = 2 ** ((octave-1 if octave > 0 else 0) + (note // 12))


    def stop(self):
        """Set this source to "finished", and close the file (if it exists)."""
        self.finished = True
        if self.file:
            self.file.close()


    @micropython.viper
    def load_from_file(self):
        """Load next data from file."""
        self.frame_idx = 0
        bytes_read = int(self.file.readinto(self.mv))
        if bytes_read == 0:
            if bool(self.loop):
                self.file.seek(self.file_start_position, 0)
                self.load_from_file()
                return
            else:
                self.stop()
        self.mv_end = bytes_read


    @staticmethod
    @micropython.viper
    def vol_2_shift(volume: int) -> int:
        """Convert volume from 0-10 into a bit shift from 16-0.

        This default implementation is designed for 16bit formats;
        it may need to be modified for other formats. 
        """
        if volume <= 0:
            return 16
        if volume >= 10:
            return 0
        return 10 - volume
